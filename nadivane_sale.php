             })
            $('.arrow-left').on('click', function(e){
                e.preventDefault();
                mySwiper.swipePrev();
              })
              $('.arrow-right').on('click', function(e){
                e.preventDefault();
                mySwiper.swipeNext();
              })
        })
        
    }
    else{
        var mySwiper = new Swiper(currentSlider, {
            scrollContainer: true,
            scrollbar: {
